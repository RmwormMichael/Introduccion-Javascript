// Diferencia entre metodo y funcion. La diferencia es que los que tienen un punto segudo del nombre de la funcion son metodos y los que tienen el nombre de la funcion seguido de los parentesis son funciones

const numero = 20;
const numero2 = '20'

console.log(parseInt(numero2));  // parseInt es una funcion
console.log(numero.toString());  // .toString es un metodo
